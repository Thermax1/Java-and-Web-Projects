package com.example.munozpage;

public class Heroes {
    private int id;
    private String name;
    private String game;

    public int getID(){
        return id;
    }

    public void setID(int newID){
        this.id = newID;
    }

    public String getName(){
        return name;
    }

    public void setName(String newName){
        this.name = newName;
    }

    public String getGame() {
        return game;
    }

    public void setGame(String newGame) {
        this.game = newGame;
    }

}
